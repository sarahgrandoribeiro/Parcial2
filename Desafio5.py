base = float(input("Digite a base do triângulo: ")) # Solicita a medida da base
altura = float(input("Digite a altura do triângulo: ")) # Solicita a medida da altura
area = (base * altura) / 2 # Aplica a fórmula matemática (Base vezes Altura dividido por dois)
print(f"A área do triângulo é: {area}") # Exibe o valor da área calculada
